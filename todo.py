# Проект: To-Do list (Менеджер задач)
# Цель: Создать терминальное приложение для управления списком задач
# Функционал: просмотр, добавление, удаление задач через меню


def show_menu():
    """Отображает главное меню с доступными действиями"""
    print("Добро пожаловать в To-Do list")
    print("1. Показать задачи")
    print("2. Добавить задачи")
    print("3. Удалить задачу")
    print("4. Выйти")


# Список для хранения задач (изначально пуст)
tasks: list[str] = []


def main():
    """Основная функция, управляющая логикой приложения"""
    # Бесконечный цикл — программа работает, пока пользователь не выйдет
    while True:
        show_menu()
        choice = input("Выбери что нужно сделать: ")

        # Просмотр задач
        if choice == "1":
            print("Показываю задачи...")
            if not tasks:
                print("Список пуст")
            else:
                print("Твои задачи")
                # enumerate(tasks, 1) нумерует задачи, начиная с 1 (а не с 0)
                for i, task in enumerate(tasks, 1):
                    print(f"{i}. {task}")

        # Добавление задачи
        elif choice == "2":
            task_add = input("Какую задачу добавляем? ")
            tasks.append(task_add)

        # Удаление задачи
        elif choice == "3":
            # Сначала показываем список для удобства
            print("Твои задачи")
            for i, task in enumerate(tasks, 1):
                print(f"{i}. {task}")

            removed_task = input("Какую задачу удаляем? ")
            # Преобразуем номер в индекс (индексы с 0, а пользователь видит с 1)
            index = int(removed_task) - 1

            # Проверяем, что индекс в пределах списка
            if 0 <= index < len(tasks):
                tasks.pop(index)
            else:
                print("Выбраное число за пределами списка задач")

        # Выход из программы
        else:
            print("Досвидания")
            break


# Точка входа: запускает main(), если скрипт выполняется напрямую
if __name__ == "__main__":
    main()
